    TeamID CHAR(3) NOT NULL,
    Team VARCHAR2(50) NOT NULL,
    League CHAR(3) NOT NULL,
    CONSTRAINT TEAM_T_PK PRIMARY KEY (TeamID)
);

CREATE TABLE ROSTER_T (
    RosterID NUMBER(4,0) NOT NULL,
    PlayerID VARCHAR2(10) NOT NULL,
    TeamID CHAR(3) NOT NULL,
    Season CHAR(7) NOT NULL,
    Jersey NUMBER(2,0) NOT NULL,
    Salary NUMBER(*,0),
    CONSTRAINT ROSTER_T_PK PRIMARY KEY (RosterID),
    CONSTRAINT ROSTER_T_FK1 FOREIGN KEY (PlayerID) REFERENCES PLAYER_T (PlayerID),
    CONSTRAINT ROSTER_T_FK2 FOREIGN KEY (TeamID) REFERENCES TEAM_T (TeamID)
);

