    PlayerID VARCHAR2(10) NOT NULL,
    Position VARCHAR2(50) NOT NULL,
    CONSTRAINT POSITION_T_PK PRIMARY KEY (PlayerID, Position),
    CONSTRAINT POSITION_T_FK FOREIGN KEY (PlayerID) REFERENCES PLAYER_T (PlayerID)
);

CREATE TABLE COLLEGE_T (
    PlayerID VARCHAR2(10) NOT NULL,
    College VARCHAR2(100) NOT NULL,
    CONSTRAINT COLLEGE_T_PK PRIMARY KEY (PlayerID, College),
    CONSTRAINT COLLEGE_T_FK FOREIGN KEY (PlayerID) REFERENCES PLAYER_T (PlayerID)
);

